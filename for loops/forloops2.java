public class forloops2 {
    public static void main(String[]args)
    {
        for(int i=1; i<=10; i+=1)
        {
            System.out.println(i);
        }
        System.out.println("");

        //Reverse

        for(int i=5; i>=1;--i)
        {
            System.out.println(i);
        }

    }    
}
